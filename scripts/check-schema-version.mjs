#!/usr/bin/env node
/**
 * check-schema-version.mjs — keep the schema-version literals in the
 * docs, README, examples, and the canonical sample map in sync with the
 * single source of truth (`CURRENT_SCHEMA_VERSION` in
 * `src/types/map.ts`).
 *
 * The code side already DRYs the version through that one constant
 * (the `RosettaMap.schema_version` type, the Zod gate, the adapter, and
 * `rosetta init`). This guards the remaining hand-written literals that
 * a `const` can't reach — the `"schema_version": N` examples scattered
 * through Markdown and the sample JSON map.
 *
 * Usage:
 *   node scripts/check-schema-version.mjs           # check (CI / verify)
 *   node scripts/check-schema-version.mjs --fix     # rewrite to current
 *
 * Scope:
 *   - Markdown (README.md, CLAUDE.md, docs/**, examples/**): only
 *     literals INSIDE fenced ``` code blocks are enforced — prose that
 *     deliberately mentions an old version (e.g. "schema_version: 1
 *     maps are rejected") is left alone.
 *   - JSON (maps/**): the canonical sample map is enforced whole-file.
 *   - Test fixtures and goldens are intentionally NOT scanned: they
 *     carry deliberate mixed/invalid versions for negative tests, and
 *     goldens are regenerated by the adapter.
 *
 * Exemptions: add an HTML comment containing `schema-keep` on the
 * matched line (or on the line just above a fence) to skip an
 * intentional non-current literal inside a code block.
 *
 * Exit code: 0 if everything matches (or was fixed); 1 if check found
 * mismatches.
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { readdirSync, statSync } from 'node:fs';
import { join, extname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { dirname } from 'node:path';

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), '..');
const FIX = process.argv.includes('--fix');

/** Read the source-of-truth constant out of src/types/map.ts. */
function currentSchemaVersion() {
    const src = readFileSync(join(repoRoot, 'src/types/map.ts'), 'utf8');
    const m = /CURRENT_SCHEMA_VERSION\s*=\s*(\d+)/.exec(src);
    if (!m) {
        throw new Error('could not find CURRENT_SCHEMA_VERSION in src/types/map.ts');
    }
    return Number.parseInt(m[1], 10);
}

/** Recursively list files under `dir` matching one of `exts`. */
function walk(dir, exts, out = []) {
    let entries;
    try {
        entries = readdirSync(dir);
    } catch {
        return out;
    }
    for (const name of entries) {
        const full = join(dir, name);
        if (statSync(full).isDirectory()) {
            walk(full, exts, out);
        } else if (exts.includes(extname(full))) {
            out.push(full);
        }
    }
    return out;
}

// Two grouped patterns: group 1 = prefix to preserve, group 2 = the
// number, (group 3 = suffix for the marker form).
//   - `"schema_version": N`, `schema_version: N`, `schema_version=N`,
//     `schema_version N`
const SCHEMA_VERSION_RE = /("?schema_version"?\s*[:=]?\s*)(\d+)/g;
//   - the marker-header form `| schema: N |`
const MARKER_SCHEMA_RE = /(\|\s*schema:\s*)(\d+)(\s*\|)/g;

const EXEMPT = 'schema-keep';

/**
 * Scan one Markdown file. Returns { mismatches, fixed } and, when FIX,
 * writes the file back.
 */
function scanMarkdown(file, current) {
    const lines = readFileSync(file, 'utf8').split('\n');
    const mismatches = [];
    let inFence = false;
    let fenceExempt = false;

    for (let i = 0; i < lines.length; i += 1) {
        const line = lines[i];
        const isFenceMarker = line.trimStart().startsWith('```');
        if (isFenceMarker) {
            if (!inFence) {
                // Opening a fence: it's exempt if the line above says so.
                fenceExempt = i > 0 && lines[i - 1].includes(EXEMPT);
            }
            inFence = !inFence;
            continue;
        }
        if (!inFence) continue;
        if (fenceExempt || line.includes(EXEMPT)) continue;

        const replaced = applyPatterns(line, current, mismatches, file, i + 1);
        if (FIX && replaced !== line) lines[i] = replaced;
    }

    if (FIX && mismatches.length > 0) {
        writeFileSync(file, lines.join('\n'));
    }
    return mismatches;
}

/** Scan one JSON file whole (the canonical sample map). */
function scanJson(file, current) {
    const text = readFileSync(file, 'utf8');
    const lines = text.split('\n');
    const mismatches = [];
    for (let i = 0; i < lines.length; i += 1) {
        const replaced = applyPatterns(lines[i], current, mismatches, file, i + 1, true);
        if (FIX && replaced !== lines[i]) lines[i] = replaced;
    }
    if (FIX && mismatches.length > 0) writeFileSync(file, lines.join('\n'));
    return mismatches;
}

/** Apply both patterns to one line, recording mismatches; returns the
 * (possibly) fixed line. `jsonOnly` restricts to the quoted form. */
function applyPatterns(line, current, mismatches, file, lineNo, jsonOnly = false) {
    let out = line;
    const record = (found) => {
        if (found !== current) {
            mismatches.push({ file, lineNo, found, text: line.trim() });
        }
    };

    out = out.replace(SCHEMA_VERSION_RE, (full, pre, num) => {
        if (jsonOnly && !pre.includes('"')) return full;
        record(Number.parseInt(num, 10));
        return pre + String(current);
    });
    if (!jsonOnly) {
        out = out.replace(MARKER_SCHEMA_RE, (full, pre, num, suf) => {
            record(Number.parseInt(num, 10));
            return pre + String(current) + suf;
        });
    }
    return out;
}

function main() {
    const current = currentSchemaVersion();

    const mdFiles = [
        join(repoRoot, 'README.md'),
        join(repoRoot, 'CLAUDE.md'),
        ...walk(join(repoRoot, 'docs'), ['.md']),
        ...walk(join(repoRoot, 'examples'), ['.md']),
    ];
    const jsonFiles = walk(join(repoRoot, 'maps'), ['.json']);

    const mismatches = [];
    for (const f of mdFiles) mismatches.push(...scanMarkdown(f, current));
    for (const f of jsonFiles) mismatches.push(...scanJson(f, current));

    if (mismatches.length === 0) {
        console.log(`schema-version: OK — all literals match CURRENT_SCHEMA_VERSION=${current}.`);
        return 0;
    }

    if (FIX) {
        console.log(`schema-version: fixed ${mismatches.length} literal(s) to ${current}.`);
        return 0;
    }

    console.error(
        `schema-version: ${mismatches.length} literal(s) do not match CURRENT_SCHEMA_VERSION=${current}:`,
    );
    for (const m of mismatches) {
        const rel = m.file.startsWith(repoRoot) ? m.file.slice(repoRoot.length + 1) : m.file;
        console.error(`  ${rel}:${m.lineNo}  found ${m.found}, expected ${current}  | ${m.text}`);
    }
    console.error("\nRun `npm run schema-version:fix` to update them (add a 'schema-keep'");
    console.error('comment to a line/fence to keep an intentional older literal).');
    return 1;
}

process.exit(main());
